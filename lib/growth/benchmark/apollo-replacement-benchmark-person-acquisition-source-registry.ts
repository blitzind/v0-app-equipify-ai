function rankScore(input: {
  named_persons_per_100: number
  verified_emails_per_100: number
  outreach_ready_companies_per_100: number
  wired: boolean
  configured: boolean
  complexity: number
  risk: number
  cost: number | null
  exhausted?: boolean
}): number {
  if (input.exhausted) return 0
  const costPenalty =
    input.cost == null ? 0 : input.cost <= 0.05 ? 0 : input.cost <= 0.25 ? 5 : 12
  return (
    input.named_persons_per_100 * 1.2 +
    input.verified_emails_per_100 * 2.5 +
    input.outreach_ready_companies_per_100 * 4 +
    (input.wired ? 8 : 0) +
    (input.configured ? 6 : 0) -
    input.complexity * 3 -
    input.risk * 2 -
    costPenalty
  )
}


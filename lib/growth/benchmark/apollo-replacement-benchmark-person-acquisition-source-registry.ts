export function formatPersonAcquisitionSourceRankingTable(
  sources: PersonAcquisitionSourceRegistryEntry[],
): Array<{
  source: string
  expected_named_persons_per_100: number
  expected_verified_emails_per_100: number
  cost_per_verified_contact_usd: number | null
  complexity: number
  risk: number
  recommendation: string
}> {
  return sources.map((s) => ({
    source: s.label,
    expected_named_persons_per_100: s.yield.named_persons_per_100,
    expected_verified_emails_per_100: s.yield.verified_emails_per_100,
    cost_per_verified_contact_usd: s.cost_per_verified_contact_usd,
    complexity: s.implementation_complexity,
    risk: s.compliance_risk,
    recommendation: s.recommendation,
  }))
}

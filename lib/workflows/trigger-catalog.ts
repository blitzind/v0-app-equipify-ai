/** Convenience: trigger label fallback (used by list rows). */
export function triggerLabel(trigger: WorkflowTriggerType | string): string {
  if (trigger in TRIGGER_CATALOG) return TRIGGER_CATALOG[trigger as WorkflowTriggerType].label
  return String(trigger).replace(/_/g, " ")
}

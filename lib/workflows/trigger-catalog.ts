/**
 * Workflow Automations Phase 1 — manager-friendly trigger catalog.
 *
 * Single source of truth for:
 *   - the human label shown in the trigger dropdown
 *   - the short description that explains *when* the trigger fires
 *   - the field paths managers can reference in conditions for that
 *     trigger (so we can render an inline cheat sheet beside the JSON
 *     editor instead of asking them to read a wiki)
 *   - a starter sample condition snippet they can paste / extend
 *
 * Keep aligned with `WorkflowTriggerType` in `lib/workflows/types.ts` and
 * the dispatch sites (e.g. `lib/workflows/dispatch.ts`,
 * `lib/prospects/status-events.ts`).
 */

import type { WorkflowTriggerType } from "@/lib/workflows/types"

export type TriggerFieldRef = {
  /** Field path the condition rule should reference. */
  path: string
  /** Short, user-facing description (no jargon). */
  description: string
  /** "string" | "number" | "boolean" | "datetime" | "enum" — informational only. */
  kind: "string" | "number" | "boolean" | "datetime" | "enum"
  /** When `kind === "enum"`, the allowed values. */
  enumValues?: string[]
}

export type TriggerCatalogEntry = {
  id: WorkflowTriggerType
  /** Concise menu label. */
  label: string
  /** "When" sentence — explains the trigger in plain English. */
  description: string
  /** Manager-friendly grouping (used by the future grouped dropdown). */
  group: "prospects" | "work-orders" | "quotes-invoices" | "maintenance" | "certificates" | "ai" | "equipment"
  /**
   * Phase availability. `"experimental"` is rendered with a small
   * "experimental — UI rules coming soon" caveat. Phase 1 marks the
   * prospect trigger as `"new"` so managers know it's freshly available.
   */
  availability: "stable" | "new" | "experimental"
  /**
   * Whether this trigger fires automatically today (i.e. there's an
   * existing emit site). When `false`, we mark it "Coming soon" and
   * disable saving rules against it. Always defaults to `true` for
   * existing triggers.
   */
  autoFiringToday: boolean
  /** Field paths usable in condition rules. */
  fieldRefs: TriggerFieldRef[]
  /** Example condition JSON the user can copy. */
  sampleCondition: string
}

const SAMPLE_AND = (rules: Array<{ field: string; op: string; value: unknown }>) =>
  JSON.stringify({ operator: "and", rules }, null, 2)

